#include <iostream>
#include <math.h>
using namespace std;


int main()
{
    int angulo1;
    int angulo2;
    int angulo3;
    cout << "ingrese el primer angulo" << endl;
    cin >> angulo1;
    cout << "ingrese el primer angulo" << endl;
    cin >> angulo2;
    cout << "ingrese el primer angulo" << endl;
    cin >> angulo3;
  
    if ((angulo1 < 100 && angulo2 < 100) || (angulo2 < 100 && angulo3 < 100) || (angulo1 < 100 && angulo3 < 100)) {

        cout << "el triangulo es Entangulo " << endl;
    }


    if (angulo1 == 100  || angulo2 == 100  || angulo3 == 100) {

        cout << "el triangulo es Suicangulo " << endl;
    }

    if (angulo1 > 100 || angulo2 > 100 || angulo3 >  100) {

        cout << "el triangulo es Raikangulo " << endl;
    }
  
    return 0;
}
